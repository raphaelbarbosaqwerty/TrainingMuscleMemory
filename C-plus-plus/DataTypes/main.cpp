#include <iostream>

using namespace std;

int main()
{
    char characterVar = 'C';
    int intVar = 333;
    float floatVar = 66.66;
    double doubleVar = 99.9999;
    bool booleanVar = (7 > 77); //False

    cout << "characterVar = " << characterVar << "\n";
    cout << "intVar = " << intVar << "\n";
    cout << "floatVar = " << floatVar << "\n";
    cout << "doubleVar = " << doubleVar << "\n";
    cout << "booleanVar = " << booleanVar << "\n";
    cout << "0 == False || 1 == True " << "\n";

    return 0;
}
